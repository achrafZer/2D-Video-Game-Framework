package controller;

/**
 * An enum used to describe directions of movement.
 */
public enum Direction {
    /**
     * Direction that indicates an upward shift
     */
    UP,
    /**
     * Direction that indicates a downward shift
     */
    DOWN,
    /**
     * Direction that indicates a shift to the right
     */
    RIGHT,
    /**
     * Direction that indicates a shift to the left
     */
    LEFT,
    /**
     * Direction that indicates an upward shift to the right
     */
    UP_RIGHT,
    /**
     * Direction that indicates a move to the top left
     */
    UP_LEFT,
    /**
     * Direction that indicates a downward shift to the right
     */
    DOWN_RIGHT,
    /**
     * Direction that indicates a downward shift to the left
     */
    DOWN_LEFT
}

